class CharArithDemo {
    public static void main(String[] args) {

        char ch;

        ch = 'X';
        System.out.println("ch contains " + ch);

        ch++; // Increament 'X' → next character → 'Y'
        System.out.println("ch contains " + ch);

        ch = 90; // ASCII Value 90 = z
        System.out.println("ch contains " + ch);
    }
}