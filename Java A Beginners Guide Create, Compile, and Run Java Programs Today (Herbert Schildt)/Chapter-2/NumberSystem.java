class NumberSystem {
    public static void main(String[] args) {

        int dec = 10;
        int bin = 0b1100;
        int oct = 011;
        int hex = 0xFF;

        System.out.println("Decimal: " + dec);
        System.out.println("Binary: " + bin);
        System.out.println("Ocatal: " + oct);
        System.out.println("Hex: " + hex);
    }
}