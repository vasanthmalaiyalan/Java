class Variable {
    public static void main(String[] args) {

        int myVar1;
        int myVar2;

        myVar1 = 1024; // variable assign value

        System.out.println("myVar1 contains " + myVar1); // + → string + value join

        myVar2 = myVar1 / 2;

        System.out.print("myVar2 contains myVar1 / 2: ");
        System.out.println(myVar2);
    } 
}